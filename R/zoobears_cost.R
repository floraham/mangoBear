#' Title ZooBears Cost
#'
#' @param price price of a zoobear
#' @param count number of bears at the zoo
#'
#' @return Returns the cost of total zoobears entered
#' @export
#'
#' @examples
zoobears_cost <- function(price, count) {
  final = price * count
  print(paste0("your zoobear costs", final, "dollars"))
}


